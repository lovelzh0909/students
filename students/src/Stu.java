public class Stu {

    private String id;
    private String name;

    public Stu(String id, String name) {
        super();
        this.id = id;
        this.name = name;
    }


    public Stu() {
        super();

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}

